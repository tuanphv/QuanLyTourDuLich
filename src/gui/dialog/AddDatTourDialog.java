    public ArrayList<ChiTietHanhKhachDTO> getChiTietHanhKhachList() {
        for (int i=0; i<dsPanelChiTiet.size(); i++) {
            dsChiTietHK.add(dsPanelChiTiet.get(i).getInput());
        }
        return dsChiTietHK;
    }

    public boolean isSave() {
        return this.save;
    }

    private void changeCard(int step) {
        cardIndex += step;
        CardLayout layout = (CardLayout) cards.getLayout();
        layout.show(cards, cardNames.get(cardIndex));
    }

    private boolean checkValidateCardThongTin() {
        // Validate maKHTour
        if (txtMaKHTour.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mã kế hoạch tour không được để trống!", "Lỗi nhập liệu",
                    JOptionPane.WARNING_MESSAGE);
            txtMaKHTour.requestFocus();
            return false;
        }
        try {
            String maKHTour = txtMaKHTour.getText().trim();
            Integer.valueOf(maKHTour);
            txtMaKHTour.setText(maKHTour);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Mã kế hoạch tour phải là số nguyên!", "Lỗi nhập liệu",
                    JOptionPane.WARNING_MESSAGE);
            txtMaKHTour.requestFocus();
            return false;
        }

        // Validate maKH
        if (txtMaKH.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mã khách hàng không được để trống!", "Lỗi nhập liệu",
                    JOptionPane.WARNING_MESSAGE);
            txtMaKH.requestFocus();
            return false;
        }
        try {
            String maKH = txtMaKH.getText().trim();
            Integer.valueOf(maKH);
            txtMaKH.setText(maKH);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Mã khách hàng phải là số nguyên!", "Lỗi nhập liệu",
                    JOptionPane.WARNING_MESSAGE);
            txtMaKH.requestFocus();
            return false;
        }
        return true;
    }

